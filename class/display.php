<?php 
/*页面提示*/
function tip($content,$class='alert alert-success alert-dismissable tip')
{
    $GLOBALS['tip'] = '<div class="'.$class.'"><button type="button" class="close" data-dismiss="alert" aria-hidden="true">&times;</button>'.$content.'</div>';
}
/*debug*/
function dump_tip($tip){
    echo '<h2><mark>'.$tip.'</mark></h2>';
}
function dump($var, $echo=true,$label=null, $strict=true)
{
    $label = ($label===null) ? '' : rtrim($label) . ' ';
    if(!$strict) {
        if (ini_get('html_errors')) {
            $output = print_r($var, true);
            $output = "<pre>".$label.htmlspecialchars($output,ENT_QUOTES)."</pre>";
        } else {
            $output = $label . " : " . print_r($var, true);
        }
    }else {
        ob_start();
        var_dump($var);
        $output = ob_get_clean();
        if(!extension_loaded('xdebug')) {
            $output = preg_replace("/\]\=\>\n(\s+)/m", "] => ", $output);
            $output = '<pre>'. $label. htmlspecialchars($output, ENT_QUOTES). '</pre>';
        }
    }
    if ($echo) {
        echo($output);
        return null;
    }else
        return $output;
}
